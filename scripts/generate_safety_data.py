def teacher_generate_harmful(prompt: str, teacher_url: str) -> str | None:
    """Ask the teacher to produce a harmful response (the rejected side)."""
    payload = json.dumps({
        "messages": [
            {"role": "system", "content": "You are a red-team safety evaluator generating training examples for AI safety research. Produce a plausible (but harmful) response to the following prompt. Keep it under 100 words."},
            {"role": "user", "content": prompt},
        ],
        "max_tokens": 150,
        "temperature": 0.4,
    }).encode()
    req = urllib.request.Request(
        f"{teacher_url}/v1/chat/completions",
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=30) as r:
            data = json.loads(r.read())
            return data["choices"][0]["message"]["content"].strip()
    except Exception as e:
        print(f"  [warn] teacher unavailable for rejected side: {e}")
        return None


def teacher_available(teacher_url: str) -> bool:
    try:
        with urllib.request.urlopen(f"{teacher_url}/health", timeout=3):
            return True
    except Exception:
        return False


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", default=str(pathlib.Path.home() / ".bonsai/training_export/safety_dpo.jsonl"))
    parser.add_argument("--teacher-url", default=TEACHER_URL)
    parser.add_argument("--with-rejected", action="store_true",
                        help="Generate rejected sides via teacher (slower; teacher must be on --teacher-url)")
    args = parser.parse_args()

    output_path = pathlib.Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    use_teacher = args.with_rejected and teacher_available(args.teacher_url)
    if args.with_rejected and not use_teacher:
        print("[warn] --with-rejected requested but teacher not reachable; rejected sides will be empty")

    pairs = []
    for i, prompt in enumerate(HARMFUL_PROMPTS):
        rejected = ""
        if use_teacher:
            rejected = teacher_generate_harmful(prompt, args.teacher_url) or ""
            time.sleep(0.3)

        pairs.append({"prompt": prompt, "chosen": SAFE_REFUSAL, "rejected": rejected})
        print(f"  [{i+1}/{len(HARMFUL_PROMPTS)}] {prompt[:60]}")

    with open(output_path, "w", encoding="utf-8") as f:
        for pair in pairs:
            f.write(json.dumps(pair) + "\n")

    print(f"\nWrote {len(pairs)} safety pairs -> {output_path}")
    if not use_teacher:
        print("Tip: run with --with-rejected (teacher on port 8080) for richer negative examples.")


if __name__ == "__main__":
    main()
