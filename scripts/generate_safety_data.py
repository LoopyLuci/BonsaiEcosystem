"""Generate constitutional DPO pairs for safety training.

Produces a JSONL file with (prompt, chosen=safe_refusal, rejected=harmful_attempt).
The rejected side is generated by the teacher at low temperature so the model
learns to distinguish a refusal from a plausible but harmful response.
Teacher is optional; if unreachable the script falls back to an empty rejected side.

Offline-safe: no HuggingFace calls.
"""
